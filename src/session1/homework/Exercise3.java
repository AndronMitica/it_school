package session1.homework;

public class Exercise3 {
    /* 3. Operations: Write a program that performs various operations (addition, subtraction, multiplication, division, and modulus) on two numbers.The numbers can be hard-coded into the program. Print the result of each operation.*/
    public static void main(String[] args) {
        double num1 = 7;
        double num2 = 3;
        System.out.println("The sum of the numbers is: " + (num1 + num2));
        System.out.println("The subtraction of the numbers is: " + (num1 - num2));
        System.out.println("The multiplication of the numbers is: " + (num1 * num2));
        System.out.println("The division of the numbers is: " + (num1 / num2));
        System.out.println("The modus of the numbers is: " + (num1 % num2));
    }
}
